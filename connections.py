    chunks = [passage[i:i+6] for i in range(0, len(passage), 6)]
    idxs = [i for i, chunk in enumerate(chunks) if chunk == '111010']
    a = sum(1 for ch in chunks[:idxs[0]] if ch == '111000' or ch == '111001')
    b = sum(1 for ch in  chunks[idxs[0] + 1:idxs[1]] if ch == '111000' or ch == '111001')
    c = sum(1 for ch in chunks[idxs[1] + 1:] if ch == '111000' or ch == '111001')
    if a == b or a == c or b == c:
        x = 8
    else:
        x = (a < b) * 1 + (a < c) * 2 + (b < c) * 4

    return x


